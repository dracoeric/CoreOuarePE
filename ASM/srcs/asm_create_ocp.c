#include "asm.h"

void	asm_create_ocp(char *ocp, int tr, int i)
{
	if (tr == 1)
		*ocp = (*ocp | 1 << (6 - 2 * i));
	if (tr == 2)
		*ocp = (*ocp | 2 << (6 - 2 * i));
	if (tr == 3)
		*ocp = (*ocp | 3 << (6 - 2 * i));
}
